public class TestingWithRefAndPrimitivesTypes {

	private String string;
	private int integer;
	private boolean bool;
	
	private TestingWithRef testing;

	
	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}

	public int getInteger() {
		return integer;
	}

	public void setInteger(int integer) {
		this.integer = integer;
	}

	public boolean isBool() {
		return bool;
	}

	public void setBool(boolean bool) {
		this.bool = bool;
	}

	public TestingWithRef getTesting() {
		return testing;
	}

	public void setTesting(TestingWithRef testing) {
		this.testing = testing;
	}

	
}
